public interface Carro {
   public abstract String getPlaca();
   public abstract String getCilindraje();
   public abstract String getMarca();
   public abstract String getcolor();
   public abstract String getTransmision();
   public abstract String getPuestos();

   public abstract void setPlaca(String placa);
   public abstract void setCilindraje(String cilindraje);
   public abstract void setMarca(String marca);
   public abstract void setcolor(String color);
   public abstract void setTransmision(String transmision);
   public abstract void setPuestos(String puestos);
}